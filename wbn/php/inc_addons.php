<tr><td><a name="brains"></a><div class="subheading">
Brains (AI robot players): <a href="#top"><img src="images/arrowup.gif" width="15" height="11" border="0" align="right"></a></div></td></tr>
<tr> 
<td><div class="bodytext">

<br>
If you are forced to leave the keyboard briefly in the middle of a game, there is no way to
'pause' WinBolo like other computer games, because there are other live human beings
out on the network playing also, and you cannot pause the universe by clicking a button.
Brains provide a way to take over if you need to leave your seat for a few minutes.
<br><br><b>Opponents or Allies</b>
If you have spare computers or a fast computer, you can run automated opponents on
them, to play against when you don’t have human opponents. If you have spare computers, you can run WinBolo on them, ally those tanks with yours,
and then hand control over to specialised ally Brains.


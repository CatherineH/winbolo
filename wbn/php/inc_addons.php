<a name="top"></a><br><center><a href="#brains">Brains</a> | <a href="#borgs">Borgs</a> | <a href="#others">Map Editors & Others</a></center>
<br>The brains section contains brain modules that offer the ability of independent tank control. The borgs section contains brain modules that offer assistance to a human tank controller. Note that some brains (in the strict sense of the word) also offer borg functionality which can be switched on and off. The last secion includes Map Editors & Viewers and Other Utilities.

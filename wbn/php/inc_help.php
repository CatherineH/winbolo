<B>How does ranking work?</B><BR>
Games are ranked using a modified version of the ELO algorithm. The ELO algorithm is used by Chess players and by the original Internet Bolo League. The algorithm can be summed at by:
<ul>
<li>When player signs up they receive an initial score of 1600</li>
<li>When a game is played the two scores are feed into the algorithm and the winners score is increased and the losers decreased.</li>
<li>The algorithm takes into an account the relative scores of the two players. For instance if a player with a high score (i.e a good player) beats a player with a low score (a not so good player) the winner will only receive a few points and the loser will only lose a couple of points. However if a low ranked player beats a high ranked player they will gain a lot of points.</li>
</ul>
<P>
<A href="#top">Back to top</A><P>

<a name="#4.2"></a>
<B>Why didn't my 1x1 get ranked?</B><BR>
As Winbolo.net is an automated ranking system and does not rely on any human game reporting certain conditions must be met for the game to be considered a valid 1x1 game. The criteria for a game to be ranked is:
<ul>
<li>One player must have captured and own all bases.</li>
<li>No non-participants can have captured any bases or pillboxes.</li>
<li>Only 2 players have played and captured any bases or pillboxes.</li>
<li>Both players must of captured at least one base or pillbox.</li>
</ul>
<P> 
<A href="#top">Back to top</A><P>

<a name="#4.3"></a>
<B>Why didn't my team get ranked?</B><BR>
As Winbolo.net is an automated ranking system and does not rely on any human game reporting certain conditions must be met for the game to be considered a valid team game. The criteria for a game to be ranked is:
<ul>
<li>One team must have captured and own all bases.</li>
<li>No non-participants can have captured any bases or pillboxes.</li>
<li>All team members on both sides must be allied at the end of the game.</li>
<li>At least one base or pillbox must be captured by two members of both teams.</li>
</ul><p>


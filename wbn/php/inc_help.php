<B>What is Bolo?</B><BR>
Bolo was created by Stuart Cheshire of Stanford University. It has evolved from a little-known tank game on the BBC Micro to an explosively successful Macintosh game that is played constantly on the Internet, as well as many smaller networks. The author summarizes his work: 

"Bolo is a 16 player graphical networked real-time multi-player tank battle game. It has elements of arcade-style shoot-em-up action, but for the serious players who play 12 hour games with 16 players working in teams in different networked computer clusters around an office or university campus, it becomes more of a strategy game. You have to play it to understand." (from <a href="http://www.lgm.com/bolo/intro/">OBHP Intro</A>)<P>
<A href="#top">Back to top</A><P>
<a name="1.2"></a>
<B>What is WinBolo?</B><BR>
WinBolo is an implemented of Bolo from scratch on the Linux and Windows platforms. Both the Linux and Windows versions are network compatible and their names are used interchangeably without this site. For more information and downloads visit the Official WinBolo site (<A href="http://www.winbolo.com/">www.winbolo.com</A>)<P>
<A href="#top">Back to top</A><P>
<a name="1.3"></a>
<B>What is WinBolo.net?</B><BR>
WinBolo.net is a real time game tracking and player ranking/statistics system for WinBolo players. You can signup for an account with Winbolo.net and use it when playing online. If the server is participating it will send game data back to Winbolo.net which will allow your ranking to be updated. Also when playing others to find what game you are in and view a real time log of events that happen in the game. You can opt whether you are going to use your Winbolo.net name in each game.<br><br>

<a name="#4.4"></a>
<B>Why does the number of games played not match the number of wins plus loses?</B><BR>
Winbolo.net will update the number of games played for every game you join and leave regardless of whether you do anything in the game at all. Winbolo.net will only update the number of wins/loses if a ranking game is played. So this number will more then likely be smaller then the number of games played. Also Winbolo.net will update the number of bases/pills/tanks for every game you play in.<p>

<center>Winbolo.net is a real time game tracking and player statistics website.</center><P>
	 The idea for Winbolo.net came around in the middle of 2000, but took a year before any work was started on it. It was originally a way to publish the #winbolo IRC server game bot results to the web as well as to display player ranks.<p>
	 Almost 3 years later Winbolo.net is finally realised. Players can sign up and share the same online profile between the game and the online community. Also importantly, there is no impact on the game as Winbolo.net player information integration is completely optional and can be decided by the player if they wish to participate on a game by game basis.<p>

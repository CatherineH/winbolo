The first thing you will want to do is setup an account at wbn (<b>w</b>in<b>b</b>olo.<b>n</b>et). Go to <a href="http://www.winbolo.net/signup.php" target="_new">http://www.winbolo.net/signup.php</a>. Fill in the form and once you submit, you will get a confirmation sent to your email.
<br><br>
</div></td></tr>

<tr><td colspan="3"></div><div class="subheading">Change winbolo nick</div><div class="bodytext">
<li>Open up winbolo and join a game. While in the game, make sure your nick is the same one you just created in wbn, if not, go to the Winbolo menu and select Change Player Name. Exit winbolo.
<li><b>OR</b> from the Internet(Tracker Search) page, click on the "Set Player Name" button and make sure the nick is the same as the one created in wbn.
